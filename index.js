
var num = prompt("Enter your number: ");

if (num >= 80) {
    console.log('You have got A+')
}
else if (num >= 70) {
    console.log('You have got A')
}
else if (num >= 60) {
    console.log('You have got A-')
}
else if (num >= 50) {
    console.log('You have got B')
}
else if (num >= 40) {
    console.log('You have got C')
}
else if (num >= 33) {
    console.log('You have got D')
}
else{
    console.log('Fail')
}




